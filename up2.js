async function 子节点识别(q, x, y, a) {
	var a = a || 0;
	var r = {};
	r[q] = x;
	var node = await acc.findViewEX(r, 50);
	if (!node) {
		//floatw.addlog("节点识别没找到:" + x);
		return -1;
	} else {
		var count = await node.getChildCount();
		var arrxy = await node.getXY(2);
		//floatw.addlog("节点识别:" + y + ",坐标:" + arrxy[0] + "子节点数:" + count);
		//return true;
		if (count > 0) {
			var node2 = await node.getChild(a);
			var arrxy = await node2.getXY(1);
			if (node2) {
				floatw.addlog("第" + a + "个子节点识别:" + y + ",坐标:" + arrxy);
				return node2
			};

		};
	}
};
