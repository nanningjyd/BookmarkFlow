// 创建一个对象来存储计时器

var timers = {};
// 开始计时器函数
async function startTimer(timerName) {
	if (!timers[timerName]) {
		timers[timerName] = {
			startTime: new Date().getTime() // 记录开始时间
		};
		//logs('计时器 "' + timerName + '" 已开始。');
	} else {
		logs('计时器 "' + timerName + '" 已经在运行中。');
	}
}
// 结束计时器函数
async function stopTimer(timerName) {
	if (timers[timerName]) {
		var elapsed = new Date().getTime() - timers[timerName].startTime; // 计算经过的时间（毫秒）
		var minutes = Math.floor(elapsed / 60000); // 转换为分钟
		var seconds = Math.floor((elapsed % 60000) / 1000); // 转换为秒钟

		//logs('计时器 "' + timerName + '" 已停止。');

		// 返回计时器经过时间的文本
		return '经过的时间：' + minutes + ' 分 ' + seconds + ' 秒。';
	} else {
		logs('计时器 "' + timerName + '" 不存在或未开始。');
		return ''; // 如果计时器不存在或未开始，则返回空字符串
	}
}


